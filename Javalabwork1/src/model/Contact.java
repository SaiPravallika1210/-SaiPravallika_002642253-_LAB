package model;

/**
 *
 * @author balin
 */
public class Contact {
    private String phone;
    private String email;
    
    
    public Contact()
    {
        this.phone="";
        this.email="";
         
 }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

   
    
}
